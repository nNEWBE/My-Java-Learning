public class Comments {
    public static void main(String[] args) {

        System.out.println("Hello, world!");
        // ? This is a single line comment
        // do Comments make the program readable
        // ! Comments are ignored by the all compilers
        // * Commnets can be written anywhere in the program
        // # Any kind of word and character is ignored by the compiler

        /*
         * This is a documentational comment
         * This is similar as single line comment
        */

        /*
        //  This is a multiline comment
        // This is similar as single line comment
        */
    }
}
